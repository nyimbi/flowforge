"""Slack request router for building-permit.

Generated by flowforge JTBD generator — do not edit by hand.

The router translates a parsed slash-command request into a POST
against ``/<url-segment>/events`` on the bundle's FastAPI backend.
Hosts subclass :class:`SlackAdapter` to wire signature verification,
identity resolution, and the concrete HTTP transport.

Slash-command grammar::

	/<slash> <event> key=value key2=value2 ...

The ``<event>`` token is required; any number of ``key=value`` pairs
become the JSON ``payload`` body.
"""

from __future__ import annotations

from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Any

from . import commands


@dataclass(frozen=True)
class SlackRequest:
	"""Minimal shape of a Slack slash-command HTTP request."""

	headers: dict[str, str]
	body: str
	command: str
	text: str
	user_id: str
	team_id: str


@dataclass(frozen=True)
class Principal:
	"""Bundle-side identity resolved from a Slack user."""

	tenant_id: str
	user_id: str


@dataclass(frozen=True)
class DispatchOutcome:
	"""Result of dispatching one slash-command request."""

	ok: bool
	status: int
	message: str
	command: commands.CommandSpec | None = None


def parse_text(text: str) -> tuple[str, dict[str, str]]:
	"""Split ``"<event> key=value ..."`` into ``(event, payload)``.

	Empty/missing event → ``("", {})``. Unknown ``key=value`` shapes are
	dropped silently; the host's response handler should surface a
	usage hint when the catalog lookup misses.
	"""

	assert isinstance(text, str), "text must be a string"
	tokens = text.split()
	if not tokens:
		return "", {}
	event = tokens[0]
	payload: dict[str, str] = {}
	for token in tokens[1:]:
		if "=" not in token:
			continue
		key, _, value = token.partition("=")
		key = key.strip()
		if not key:
			continue
		payload[key] = value
	return event, payload


class SlackAdapter(ABC):
	"""Abstract base for the host's concrete Slack wiring."""

	@abstractmethod
	def verify_signature(self, headers: dict[str, str], body: str) -> bool:
		"""Return True iff the request carries a valid Slack signature."""

	@abstractmethod
	def resolve_principal(self, slack_user_id: str, slack_team_id: str) -> Principal:
		"""Bridge a Slack identity to the bundle's tenant + user."""

	@abstractmethod
	async def post_event(
		self,
		url_segment: str,
		event: str,
		payload: dict[str, Any],
		principal: Principal,
	) -> dict[str, Any]:
		"""POST a workflow event to the FastAPI backend on behalf of *principal*."""

	async def dispatch(self, request: SlackRequest) -> DispatchOutcome:
		"""Translate a Slack slash-command request into a backend POST."""

		assert isinstance(request, SlackRequest), "request must be a SlackRequest"
		if not self.verify_signature(request.headers, request.body):
			return DispatchOutcome(ok=False, status=401, message="invalid signature")
		# Slack delivers the slash with a leading "/"; the catalog stores
		# the bare token, so strip exactly one leading slash.
		slash = request.command.lstrip("/")
		event, payload = parse_text(request.text)
		if not event:
			return DispatchOutcome(ok=False, status=400, message="missing event token")
		spec = commands.lookup(slash, event)
		if spec is None:
			return DispatchOutcome(
				ok=False,
				status=404,
				message=f"unknown command /{slash} {event}",
			)
		principal = self.resolve_principal(request.user_id, request.team_id)
		body = await self.post_event(spec.url_segment, spec.event, payload, principal)
		return DispatchOutcome(
			ok=True,
			status=200,
			message=str(body.get("state", "ok")),
			command=spec,
		)
