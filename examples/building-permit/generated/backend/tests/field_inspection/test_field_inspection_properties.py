"""Property tests for the field_inspection workflow.

Generated by ``flowforge_cli.jtbd.generators.property_tests`` per
ADR-003 (``docs/v0.3.0-engineering/adr/ADR-003-hypothesis-seed-pinning.md``).

The seed is pinned at *generation* time, not at test-run time, so it is
visible directly in this source file and stable across hosts:

	_SEED = int(sha256(JTBD_ID.encode()).hexdigest()[:8], 16) = 723059501

Hypothesis is pinned to ``>=6.100,<7.0`` in the ``flowforge-cli``
project metadata (see ``flowforge-cli/pyproject.toml``). Together those
two pins make property-test failures reproducible byte-for-byte across
hosts: a counter-example printed locally is the same one CI prints.

Properties asserted (per ``docs/improvements.md`` item 3):

* Any legal event sequence terminates in a known workflow state.
* The simulator's audit-event chain stays monotonic.
* Every fire commits its effects atomically or returns a no-op result;
  no half-applied state leaks into the next fire.
* No orphan entities — every ``created_entities`` row is paired with a
  fired transition that declared a ``create_entity`` effect.
"""

from __future__ import annotations

import asyncio
import hashlib
import json
from pathlib import Path
from typing import Any

from hypothesis import HealthCheck, given, seed, settings, strategies as st

from flowforge.dsl import WorkflowDef
from flowforge.replay.simulator import simulate


# ---------------------------------------------------------------------------
# Pinned seed (ADR-003) — materialised at generation time as a literal int
# below so it shows up under grep + so the test runs the same input space
# on every host.  Regenerate this file via ``flowforge jtbd-generate`` if
# the JTBD id changes; the assert below catches accidental drift.
# ---------------------------------------------------------------------------

JTBD_ID = "field_inspection"
_SEED = 723059501  # 0x2b19032d = int(sha256(JTBD_ID.encode()).hexdigest()[:8], 16)
assert _SEED == int(hashlib.sha256(JTBD_ID.encode()).hexdigest()[:8], 16), (
	"ADR-003 seed drift: regenerate this file via `flowforge jtbd-generate`"
)

INITIAL_STATE = "intake"

KNOWN_STATES: frozenset[str] = frozenset((
	"done",
	"escalated",
	"intake",
	"rejected",
	"review",
))

TERMINAL_STATES: frozenset[str] = frozenset((
	"done",
	"rejected",
))

WORKFLOW_EVENTS: tuple[str, ...] = (
	"approve",
	"escalate",
	"reject",
	"submit",
)

# Guard variables stripped of the leading ``context.`` prefix; the
# strategy below builds an ``initial_context`` dict shaped so the
# evaluator's ``{"var": "context.<name>"}`` lookup resolves them.
GUARD_VARS: tuple[str, ...] = (
)


# ---------------------------------------------------------------------------
# Workflow-definition loader (mirrors ``test_simulation.py``)
# ---------------------------------------------------------------------------


def _find_def_path() -> Path:
	"""Walk up from this file until ``workflows/<jtbd>/definition.json`` exists."""

	cur = Path(__file__).resolve()
	for parent in (cur, *cur.parents):
		candidate = parent / "workflows" / JTBD_ID / "definition.json"
		if candidate.is_file():
			return candidate
	raise FileNotFoundError(
		"workflows/" + JTBD_ID + "/definition.json not found relative to "
		+ str(cur)
	)


_DEF_PATH = _find_def_path()


def _load() -> WorkflowDef:
	raw = json.loads(_DEF_PATH.read_text(encoding="utf-8"))
	return WorkflowDef.model_validate(raw)


# ---------------------------------------------------------------------------
# Hypothesis strategies
# ---------------------------------------------------------------------------


def _ctx_strategy() -> st.SearchStrategy[dict[str, Any]]:
	"""Boolean draws for each guard variable; nested under ``context``.

	The synthesised guards always read ``{"var": "context.<name>"}`` (see
	``transforms.derive_transitions``), so the test feeds ``simulate``
	an ``initial_context`` shaped ``{"context": {<name>: <bool>, ...}}``.
	"""

	if not GUARD_VARS:
		return st.just({})
	return st.builds(
		lambda inner: {"context": inner},
		st.fixed_dictionaries({v: st.booleans() for v in GUARD_VARS}),
	)


def _event_sequence_strategy() -> st.SearchStrategy[list[tuple[str, dict[str, Any]]]]:
	"""Bounded sequence of ``(event, payload)`` tuples drawn from the workflow."""

	if not WORKFLOW_EVENTS:
		return st.just([])
	return st.lists(
		st.sampled_from(WORKFLOW_EVENTS).map(lambda e: (e, {})),
		min_size=0,
		max_size=12,
	)


# ADR-003 contract:
#   @hypothesis.seed(_SEED)        — pins the per-JTBD seed
#   @hypothesis.settings(...)      — pins derandomize + max_examples
# ``seed`` is a separate decorator from ``settings`` in hypothesis 6.x;
# stacking the two reproduces the ADR's intent (deterministic input
# space, 200 examples, no per-host RNG drift) on the published API.
_PROP_SETTINGS = settings(
	derandomize=True,
	max_examples=200,
	deadline=None,
	suppress_health_check=[HealthCheck.too_slow, HealthCheck.function_scoped_fixture],
)
_PROP_SEED = seed(_SEED)


def _run(events: list[tuple[str, dict[str, Any]]], initial_context: dict[str, Any]) -> Any:
	"""Drive the simulator inside a fresh event loop (no pytest-asyncio dep)."""

	wd = _load()
	loop = asyncio.new_event_loop()
	try:
		return loop.run_until_complete(
			simulate(
				wd,
				initial_context=initial_context,
				events=events,
				tenant_id="prop-tenant",
			)
		)
	finally:
		loop.close()


# ---------------------------------------------------------------------------
# Property 1 — every legal sequence terminates in a known state
# ---------------------------------------------------------------------------


@_PROP_SETTINGS
@_PROP_SEED
@given(events=_event_sequence_strategy(), context=_ctx_strategy())
def test_property_sequence_terminates(
	events: list[tuple[str, dict[str, Any]]],
	context: dict[str, Any],
) -> None:
	"""Bounded sequences must land in a state declared by the workflow."""

	result = _run(events, context)
	assert result.terminal_state in KNOWN_STATES, (
		"unknown terminal state " + repr(result.terminal_state)
		+ "; history=" + repr(result.history)
	)


# ---------------------------------------------------------------------------
# Property 2 — audit chain is monotonic
# ---------------------------------------------------------------------------


@_PROP_SETTINGS
@_PROP_SEED
@given(events=_event_sequence_strategy(), context=_ctx_strategy())
def test_property_audit_chain_monotonic(
	events: list[tuple[str, dict[str, Any]]],
	context: dict[str, Any],
) -> None:
	"""Audit events emitted across a fire-sequence keep insertion order.

	The simulator extends ``audit_events`` per fire; we reconstruct the
	per-event index and assert it is strictly increasing. A regression
	here would mean two fires interleaved their audit appends.
	"""

	result = _run(events, context)
	indices: list[int] = []
	cursor = 0
	for fr in result.fire_results:
		fr_audits = list(fr.audit_events)
		# Each fire's audits land at the tail; reconstruct their global
		# index so we can assert the chain is contiguous + monotonic.
		for _ in fr_audits:
			indices.append(cursor)
			cursor += 1
	assert indices == sorted(indices), "audit chain non-monotonic: " + repr(indices)
	assert cursor == len(result.audit_events), (
		"audit-event accounting drift: cursor=" + repr(cursor)
		+ " total=" + repr(len(result.audit_events))
	)


# ---------------------------------------------------------------------------
# Property 3 — every fire is atomic-or-rolled-back
# ---------------------------------------------------------------------------


@_PROP_SETTINGS
@_PROP_SEED
@given(events=_event_sequence_strategy(), context=_ctx_strategy())
def test_property_fire_atomic_or_rolled_back(
	events: list[tuple[str, dict[str, Any]]],
	context: dict[str, Any],
) -> None:
	"""Every fire either commits effects atomically or no-ops cleanly.

	When the engine cannot match a transition (no candidate or all
	guards fail) it returns a ``FireResult`` with
	``matched_transition_id=None`` and an empty ``audit_events`` list
	without touching the snapshot. When a transition matches, the
	snapshot has already been committed before the simulator records
	the result, so the post-fire state (``new_state``) is one of the
	workflow's declared states.
	"""

	result = _run(events, context)
	for fr in result.fire_results:
		if fr.matched_transition_id is None:
			# No-op fire: audit must be empty (engine didn't emit
			# anything) and the state must still be a known one.
			assert list(fr.audit_events) == [], (
				"no-op fire emitted audit events: " + repr(fr.audit_events)
			)
			assert fr.new_state in KNOWN_STATES, (
				"no-op fire landed on unknown state: " + repr(fr.new_state)
			)
		else:
			# Committed fire: post-fire state is the declared target;
			# audit-event list is well-defined (possibly empty when the
			# transition itself declared no audit effect).
			assert fr.new_state in KNOWN_STATES, (
				"committed fire landed on unknown state: " + repr(fr.new_state)
			)


# ---------------------------------------------------------------------------
# Property 4 — no orphan entities
# ---------------------------------------------------------------------------


@_PROP_SETTINGS
@_PROP_SEED
@given(events=_event_sequence_strategy(), context=_ctx_strategy())
def test_property_no_orphan_entities(
	events: list[tuple[str, dict[str, Any]]],
	context: dict[str, Any],
) -> None:
	"""Entity creations are bounded by ``create_entity`` effects fired.

	Each ``created_entities`` entry must trace back to a transition
	whose ``effects`` declared ``kind == "create_entity"``. The number
	of created entities cannot exceed the number of such effects fired
	across the sequence; a regression means a half-applied fire leaked
	an orphan into the instance.
	"""

	result = _run(events, context)
	create_effects_fired = 0
	for fr in result.fire_results:
		if fr.matched_transition_id is None:
			continue
		for eff in fr.planned_effects or ():
			kind = getattr(eff, "kind", None)
			if kind is None and isinstance(eff, dict):
				kind = eff.get("kind")
			if kind == "create_entity":
				create_effects_fired += 1
	assert len(result.created_entities) <= create_effects_fired, (
		"orphan entities: " + repr(len(result.created_entities))
		+ " created vs " + repr(create_effects_fired)
		+ " create_entity effects fired"
	)
