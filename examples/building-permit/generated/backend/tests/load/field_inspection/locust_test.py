from __future__ import annotations

import os

from locust import HttpUser, between, events, task


_TENANT_ID = os.environ.get("TENANT_ID", "sla-stress-tenant")
_BUDGET_MS = 172800000
_P95_MS = 2000


class FieldInspectionSlaUser(HttpUser):
	"""Fires ``POST /field-inspection/events`` at the SLA-implied rate."""

	wait_time = between(0.5, 1.5)

	@task
	def fire_event(self) -> None:
		self.client.post(
			"/field-inspection/events",
			json={"event": "submit", "payload": {}, "tenant_id": _TENANT_ID},
			headers={"Idempotency-Key": f"locust-{id(self)}-{self.environment.runner.user_count}"},
		)


@events.test_stop.add_listener
def _assert_p95(environment, **_kwargs: object) -> None:
	"""Assert p95 < threshold; exit non-zero on breach so CI captures it."""

	stats = environment.stats.total
	p95 = stats.get_response_time_percentile(0.95)
	if p95 is None:
		return
	if p95 > _P95_MS:
		environment.process_exit_code = 1
		print(f"SLA STRESS FAIL: p95={p95}ms > threshold={_P95_MS}ms (budget={_BUDGET_MS}ms)")
		return
	print(f"SLA STRESS OK: p95={p95}ms <= threshold={_P95_MS}ms (budget={_BUDGET_MS}ms)")
