"""Domain service for Approve or Deny Building Permit.

Coordinates the SQLAlchemy model + workflow adapter. Generated by
flowforge JTBD generator from ``jtbd.permit_decision``.

v0.3.0 W2 / item 6 — the matching router enforces ``Idempotency-Key``
before this service is ever called; the dedupe lookup lives in
``..permit_decision.idempotency``. The service itself stays
authoring-agnostic so unit tests that bypass the router (and therefore
the idempotency gate) keep working.
"""

from __future__ import annotations

from contextlib import contextmanager
from datetime import datetime, timezone
from typing import Any, Iterator

from flowforge.ports.types import Principal

from ..adapters.permit_decision_adapter import fire_event
from ..permit_decision.idempotency import IDEMPOTENCY_TTL_HOURS
from ..models.permit_decision import PermitDecision


# v0.3.0 W2 / item 12 — OpenTelemetry by construction. Lazy import so
# the generated app runs without ``opentelemetry-api`` installed.
try:  # pragma: no cover - import-time fast path
	from opentelemetry import trace as _otel_trace
	_OTEL_TRACER: Any = _otel_trace.get_tracer("flowforge.permit_decision")
except ImportError:  # pragma: no cover
	_OTEL_TRACER = None


@contextmanager
def _otel_span(name: str, attributes: dict[str, Any]) -> Iterator[Any]:
	if _OTEL_TRACER is None:
		yield None
		return
	with _OTEL_TRACER.start_as_current_span(name, attributes=attributes) as span:
		yield span


class PermitDecisionService:
	"""Application service for the ``permit_decision`` JTBD."""

	async def submit(
		self,
		payload: dict[str, Any],
		*,
		principal: Principal,
		tenant_id: str = "default",
	) -> dict[str, Any]:
		"""Create a new ``PermitDecision`` and fire the ``submit`` event."""

		assert isinstance(payload, dict)
		now = datetime.now(timezone.utc)
		entity = PermitDecision(
			id=payload.get("id", ""),
			tenant_id=tenant_id,
			state="intake",
			created_at=now,
			updated_at=now,
		)
		span_attrs: dict[str, Any] = {
			"flowforge.tenant_id": tenant_id,
			"flowforge.jtbd_id": "permit_decision",
			"flowforge.state": "intake",
			"flowforge.event": "submit",
			"flowforge.principal_user_id": principal.user_id,
		}
		with _otel_span("flowforge.service.submit", span_attrs) as _span:
			result = await fire_event(
				"submit",
				payload=payload,
				principal=principal,
				tenant_id=tenant_id,
			)
			if _span is not None:
				_span.set_attribute("flowforge.new_state", result.new_state)
		return {
			"id": entity.id,
			"state": result.new_state,
			"audit": [
				evt.model_dump() if hasattr(evt, "model_dump") else dict(evt)
				for evt in result.audit_events
			],
		}

	async def transition(
		self,
		event: str,
		payload: dict[str, Any],
		*,
		principal: Principal,
		tenant_id: str = "default",
	) -> dict[str, Any]:
		"""Generic transition driver — used by the router for review/approve/etc."""

		span_attrs: dict[str, Any] = {
			"flowforge.tenant_id": tenant_id,
			"flowforge.jtbd_id": "permit_decision",
			"flowforge.event": event,
			"flowforge.principal_user_id": principal.user_id,
		}
		with _otel_span("flowforge.service.transition", span_attrs) as _span:
			result = await fire_event(
				event,
				payload=payload,
				principal=principal,
				tenant_id=tenant_id,
			)
			if _span is not None:
				_span.set_attribute("flowforge.new_state", result.new_state)
		return {"state": result.new_state, "matched": bool(result.matched_transition_id)}
