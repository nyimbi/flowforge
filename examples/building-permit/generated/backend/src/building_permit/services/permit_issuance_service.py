"""Domain service for Issue the Building Permit Certificate.

Coordinates the SQLAlchemy model + workflow adapter. Generated by
flowforge JTBD generator from ``jtbd.permit_issuance``.
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Any

from flowforge.ports.types import Principal

from ..adapters.permit_issuance_adapter import fire_event
from ..models.permit_issuance import PermitIssuance


class PermitIssuanceService:
	"""Application service for the ``permit_issuance`` JTBD."""

	async def submit(
		self,
		payload: dict[str, Any],
		*,
		principal: Principal,
		tenant_id: str = "default",
	) -> dict[str, Any]:
		"""Create a new ``PermitIssuance`` and fire the ``submit`` event."""

		assert isinstance(payload, dict)
		now = datetime.now(timezone.utc)
		entity = PermitIssuance(
			id=payload.get("id", ""),
			tenant_id=tenant_id,
			state="intake",
			created_at=now,
			updated_at=now,
		)
		result = await fire_event(
			"submit",
			payload=payload,
			principal=principal,
			tenant_id=tenant_id,
		)
		return {
			"id": entity.id,
			"state": result.new_state,
			"audit": [
				evt.model_dump() if hasattr(evt, "model_dump") else dict(evt)
				for evt in result.audit_events
			],
		}

	async def transition(
		self,
		event: str,
		payload: dict[str, Any],
		*,
		principal: Principal,
		tenant_id: str = "default",
	) -> dict[str, Any]:
		"""Generic transition driver — used by the router for review/approve/etc."""

		result = await fire_event(
			event,
			payload=payload,
			principal=principal,
			tenant_id=tenant_id,
		)
		return {"state": result.new_state, "matched": bool(result.matched_transition_id)}
