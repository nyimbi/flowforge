def fingerprint_request(body: dict[str, Any]) -> str:
	"""Deterministic fingerprint of the request body for replay-mismatch checks.

	If a caller submits the same key with a different body, the helper
	can detect the mismatch and return 409 instead of silently replaying
	the cached response.
	"""

	assert isinstance(body, dict), "body must be a dict"
	canonical = json.dumps(body, sort_keys=True, separators=(",", ":"))
	return hashlib.sha256(canonical.encode("utf-8")).hexdigest()


