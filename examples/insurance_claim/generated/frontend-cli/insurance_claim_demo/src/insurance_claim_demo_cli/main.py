"""Typer CLI entry point for insurance-claim-demo.

Generated by flowforge JTBD generator — do not edit by hand. Each JTBD
becomes a Typer subapp; each transition event becomes a command under
that subapp. The command tree mirrors the bundle's ``openapi.yaml``
(W1, item 8) one-to-one.

Run ``insurance-claim-demo-cli --help`` to enumerate.
"""

from __future__ import annotations

import json
from typing import Any, Optional

import typer

from . import client


app = typer.Typer(
	help="Typer CLI client for insurance-claim-demo — fires workflow events into the bundle's FastAPI backend.",
	no_args_is_help=True,
)

claim_intake_app = typer.Typer(
	help="File an insurance claim (FNOL)",
	no_args_is_help=True,
)
app.add_typer(claim_intake_app, name="claim-intake")


@claim_intake_app.command("approve")
def claim_intake_approve(
	claimant_name: str = typer.Option(..., help="Claimant full name (required)"),
	contact_email: str = typer.Option(..., help="Contact email (required)"),
	contact_phone: Optional[str] = typer.Option(None, help="Contact phone"),
	loss_amount: float = typer.Option(..., help="Estimated loss amount (required)"),
	loss_date: str = typer.Option(..., help="Date of loss (required)"),
	loss_description: str = typer.Option(..., help="Description of loss (required)"),
	policy_number: str = typer.Option(..., help="Policy number (required)"),
	base_url: str = typer.Option("http://localhost:8000", "--base-url", help="FastAPI backend base URL."),
) -> None:
	"""Fire ``approve`` against ``/claim-intake/events`` for File an insurance claim (FNOL)."""

	payload: dict[str, Any] = {}
	payload["claimant_name"] = claimant_name
	payload["contact_email"] = contact_email
	if contact_phone is not None:
		payload["contact_phone"] = contact_phone
	payload["loss_amount"] = loss_amount
	payload["loss_date"] = loss_date
	payload["loss_description"] = loss_description
	payload["policy_number"] = policy_number
	result = client.fire_sync(base_url, "claim-intake", "approve", payload)
	typer.echo(json.dumps(result, indent=2, sort_keys=True))


