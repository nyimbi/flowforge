"""Closed analytics-event taxonomy for insurance-claim-demo.

Generated by flowforge JTBD generator. Item 16 of docs/improvements.md.
The Step component lifecycle hooks emit these names through
``flowforge.ports.analytics.AnalyticsPort``; hosts wire that port to a
real provider (Segment / Mixpanel / Amplitude / a noop sink).

Closed: any string not in :class:`AnalyticsEvent` is, by definition,
not part of this bundle's taxonomy. Dashboards can statically validate
their event list against this enum.
"""

from __future__ import annotations

from enum import StrEnum


class AnalyticsEvent(StrEnum):
	"""Every analytics event a JTBD in this bundle may fire."""

	CLAIM_INTAKE_FIELD_FOCUSED = "claim_intake.field_focused"
	CLAIM_INTAKE_FIELD_COMPLETED = "claim_intake.field_completed"
	CLAIM_INTAKE_VALIDATION_FAILED = "claim_intake.validation_failed"
	CLAIM_INTAKE_SUBMISSION_STARTED = "claim_intake.submission_started"
	CLAIM_INTAKE_SUBMISSION_SUCCEEDED = "claim_intake.submission_succeeded"
	CLAIM_INTAKE_FORM_ABANDONED = "claim_intake.form_abandoned"


def is_known(event: str) -> bool:
	"""``True`` if *event* belongs to the closed taxonomy."""

	return event in {e.value for e in AnalyticsEvent}
