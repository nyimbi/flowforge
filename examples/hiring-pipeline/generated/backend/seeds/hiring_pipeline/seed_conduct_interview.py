"""Faker-driven seed data for Conduct interview loop.

Generated by flowforge JTBD generator from ``jtbd.conduct_interview`` per
v0.3.0 W4a / item 14. Faker is seeded deterministically from
``int(sha256("hiring_pipeline:conduct_interview")[:8], 16)`` so two
calls to ``seed()`` against the same database produce byte-identical
seed rows.

Usage::

	import asyncio
	from hiring_pipeline.conduct_interview.seed import seed
	asyncio.run(seed())

Or via the canonical Make target::

	make seed

The seed function loads through the generated service layer so RLS,
audit chain, and permissions engage exactly as a real client would —
the seed loop is just a deterministic Faker-driven harness.
"""

from __future__ import annotations

import asyncio
from datetime import timezone
from typing import Any

from faker import Faker

from flowforge.ports.types import Principal

# Absolute import — the generated host puts ``backend/src`` on
# PYTHONPATH for normal app launch; the seed loader assumes the same.
from hiring_pipeline.services.conduct_interview_service import (
	ConductInterviewService,
)


# Deterministic Faker seed: int(sha256("hiring_pipeline:conduct_interview")[:8], 16).
# Pinned at generation time so seed rows are byte-stable across runs.
FAKER_SEED: int = 3866554590

# Number of seed rows emitted per reachable forward state. Pinned at
# 10 per docs/improvements.md item 14.
ROWS_PER_STATE: int = 10

# Default tenant the seed loader writes under. Override at the call
# site via ``seed(tenant_id=...)`` for multi-tenant smoke runs.
SEED_TENANT_ID: str = "default"


# ``(state, events_after_submit)`` pairs derived from the synthesised
# transitions of ``jtbd.conduct_interview``. ``service.submit()`` always
# fires the ``submit`` event itself, so the suffixes here are the
# extra transitions needed to land an entity in the target state.
SEED_PATHS: tuple[tuple[str, tuple[str, ...]], ...] = (
	("review", ()),
	("reschedule_or_withdraw", ()),
	("done", ("approve",)),
)


def _build_payload(faker: Faker) -> dict[str, Any]:
	"""Build one realistic payload by drawing from the seeded *faker*.

	Pure data — no IO. The values are stable when the caller seeds the
	faker instance with :data:`FAKER_SEED` and consumes the same number
	of draws between calls.
	"""

	return {
		"panel_scorecard": faker.text(max_nb_chars=2000),
		"technical_score": faker.pyint(),
		"culture_score": faker.pyint(),
		"debrief_notes": faker.text(max_nb_chars=2000),
		"interview_date": faker.date_time_between(start_date='-2y', end_date='now', tzinfo=timezone.utc),
	}


async def seed(
	*,
	tenant_id: str = SEED_TENANT_ID,
	rows_per_state: int = ROWS_PER_STATE,
) -> list[dict[str, Any]]:
	"""Insert ``rows_per_state`` instances per reachable forward state.

	Returns the list of created instance summaries. The service layer
	engages RLS, audit chain, and permissions exactly as a real client
	would — the seed loop is a deterministic Faker-driven harness, not
	a bypass.
	"""

	faker = Faker()
	faker.seed_instance(FAKER_SEED)

	principal = Principal(user_id="seed-loader", roles=("seed",), is_system=True)
	service = ConductInterviewService()

	created: list[dict[str, Any]] = []
	for state, events in SEED_PATHS:
		for _ in range(rows_per_state):
			payload = _build_payload(faker)
			summary = await service.submit(
				payload,
				principal=principal,
				tenant_id=tenant_id,
			)
			for event in events:
				summary = await service.transition(
					event,
					payload,
					principal=principal,
					tenant_id=tenant_id,
				)
			summary = dict(summary)
			summary["seeded_state"] = state
			created.append(summary)
	return created


if __name__ == "__main__":  # pragma: no cover
	asyncio.run(seed())
