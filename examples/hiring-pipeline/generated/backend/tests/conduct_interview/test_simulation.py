"""Simulation tests for the conduct_interview workflow.

Generated by flowforge JTBD generator. The tests load the JSON DSL
definition + run :func:`flowforge.replay.simulator.simulate` against a
small set of canned event sequences. No DB, no FS writes, no network.
"""

from __future__ import annotations

import asyncio
import json
from pathlib import Path

from flowforge.dsl import WorkflowDef
from flowforge.replay.simulator import simulate


def _find_def_path() -> Path:
	"""Walk up from this file until ``workflows/<jtbd>/definition.json`` exists.

	Generators emit tests under ``backend/tests/<jtbd>/`` and definitions
	under ``workflows/<jtbd>/``; the relative depth depends on whether the
	host re-roots the layout, so we search rather than hard-code.
	"""

	cur = Path(__file__).resolve()
	for parent in (cur, *cur.parents):
		candidate = parent / "workflows" / "conduct_interview" / "definition.json"
		if candidate.is_file():
			return candidate
	raise FileNotFoundError(
		"workflows/conduct_interview/definition.json not found relative to "
		f"{cur}"
	)


_DEF_PATH = _find_def_path()


def _load() -> WorkflowDef:
	raw = json.loads(_DEF_PATH.read_text(encoding="utf-8"))
	return WorkflowDef.model_validate(raw)


def test_definition_parses() -> None:
	wd = _load()
	assert wd.key == "conduct_interview"
	assert wd.initial_state == "intake"


def test_happy_path_reaches_done() -> None:
	wd = _load()
	loop = asyncio.new_event_loop()
	try:
		result = loop.run_until_complete(
			simulate(
				wd,
				events=[("submit", {}), ("approve", {})],
				tenant_id="test-tenant",
			)
		)
	finally:
		loop.close()
	assert result.terminal_state == "done", result.history
	assert any(
		evt.kind.endswith(".transitioned") for evt in result.audit_events
	), "expected at least one transition audit event"


def test_branches_reachable() -> None:
	"""Each derived edge_case branch is reachable via a guarded ``submit``.

	Without context flags set the highest-priority guarded branch will not
	fire, so the engine falls through to the default ``submit`` transition
	and lands on ``review``.
	"""

	wd = _load()
	loop = asyncio.new_event_loop()
	try:
		result = loop.run_until_complete(
			simulate(wd, events=[("submit", {})], tenant_id="test-tenant")
		)
	finally:
		loop.close()
	assert result.terminal_state in {
		"review",
		"reschedule_or_withdraw",
	}, result.history
