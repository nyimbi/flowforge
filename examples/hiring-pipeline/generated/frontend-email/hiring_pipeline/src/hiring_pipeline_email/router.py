"""Email request router for hiring-pipeline.

Generated by flowforge JTBD generator — do not edit by hand.

The router consumes a parsed RFC-5322 message, walks the reply-subject
grammar (``[<jtbd>:<event>:<instance_id>]``) and POSTs against
``/<url-segment>/events`` on the bundle's FastAPI backend. Hosts
subclass :class:`EmailAdapter` to wire DKIM/SPF/DMARC, identity
resolution, and the concrete HTTP transport.
"""

from __future__ import annotations

from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Any

from . import inbound


@dataclass(frozen=True)
class InboundMessage:
	"""Minimal shape of a parsed inbound email."""

	from_address: str
	subject: str
	body: str
	headers: dict[str, str]


@dataclass(frozen=True)
class Principal:
	"""Bundle-side identity resolved from the inbound message."""

	tenant_id: str
	user_id: str


@dataclass(frozen=True)
class DispatchOutcome:
	"""Result of dispatching one inbound email."""

	ok: bool
	status: int
	message: str
	route: inbound.RouteSpec | None = None
	instance_id: str | None = None


class EmailAdapter(ABC):
	"""Abstract base for the host's concrete email wiring."""

	@abstractmethod
	def verify_envelope(self, message: InboundMessage) -> bool:
		"""DKIM/SPF/DMARC + From-header validation."""

	@abstractmethod
	def resolve_principal(self, message: InboundMessage) -> Principal:
		"""Bridge the sender's email to the bundle's tenant + user."""

	@abstractmethod
	async def post_event(
		self,
		url_segment: str,
		event: str,
		payload: dict[str, Any],
		principal: Principal,
		instance_id: str,
	) -> dict[str, Any]:
		"""POST a workflow event to the FastAPI backend on behalf of *principal*."""

	async def dispatch(self, message: InboundMessage) -> DispatchOutcome:
		"""Translate an inbound email into a backend POST."""

		assert isinstance(message, InboundMessage), "message must be an InboundMessage"
		if not self.verify_envelope(message):
			return DispatchOutcome(ok=False, status=401, message="envelope rejected")
		parsed = inbound.parse_subject(message.subject)
		if parsed is None:
			return DispatchOutcome(
				ok=False,
				status=400,
				message="subject missing reply token",
			)
		jtbd, event, instance_id = parsed
		route = inbound.lookup(jtbd, event)
		if route is None:
			return DispatchOutcome(
				ok=False,
				status=404,
				message=f"unknown route {jtbd}:{event}",
				instance_id=instance_id,
			)
		payload: dict[str, Any] = dict(inbound.parse_body(message.body))
		principal = self.resolve_principal(message)
		body = await self.post_event(
			route.url_segment,
			route.event,
			payload,
			principal,
			instance_id,
		)
		return DispatchOutcome(
			ok=True,
			status=200,
			message=str(body.get("state", "ok")),
			route=route,
			instance_id=instance_id,
		)
