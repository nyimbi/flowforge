"""Outbound email templates for hiring-pipeline.

Generated by flowforge JTBD generator — do not edit by hand. One
template per audit topic the bundle aggregates; hosts substitute
``{tenant_id}`` / ``{instance_id}`` / ``{actor}`` / ``{reply_token}``
placeholders at send time.

The reply grammar suffix ``[<jtbd>:<event>:<instance_id>]`` is appended
by the host's notification adapter (the audit topic alone does not
imply which event the recipient should fire on reply — that's a
business decision the host owns).
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class EmailTemplate:
	"""One outbound notification template, keyed by audit topic."""

	topic: str
	subject_template: str
	body_template: str


