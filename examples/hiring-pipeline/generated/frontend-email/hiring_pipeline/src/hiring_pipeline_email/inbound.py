# Subject-suffix grammar:  [<jtbd>:<event>:<instance_id>]
# Captured groups: 1 = jtbd, 2 = event, 3 = instance_id.
_SUBJECT_RE = re.compile(r"\[([a-z][a-z0-9_]*):([a-z][a-z0-9_]*):([^\]]+)\]")


def parse_subject(subject: str) -> tuple[str, str, str] | None:
	"""Return ``(jtbd, event, instance_id)`` if *subject* carries the
	flowforge reply-subject suffix, else ``None``."""

	assert isinstance(subject, str), "subject must be a string"
	match = _SUBJECT_RE.search(subject)
	if match is None:
		return None
	return match.group(1), match.group(2), match.group(3)


def parse_body(body: str) -> dict[str, str]:
	"""Parse a ``key: value`` reply body into a payload dict.

	The grammar is intentionally simple — one assignment per line, the
	first ``:`` is the separator, leading/trailing whitespace stripped.
	Lines without ``:`` are dropped silently. Quoted values stay quoted;
	the host's payload-validation layer takes the JSON cast.
	"""

	assert isinstance(body, str), "body must be a string"
	out: dict[str, str] = {}
	for line in body.splitlines():
		if ":" not in line:
			continue
		key, _, value = line.partition(":")
		key = key.strip()
		value = value.strip()
		if not key:
			continue
		out[key] = value
	return out


def lookup(jtbd: str, event: str) -> RouteSpec | None:
	"""Return the catalog entry matching ``(jtbd, event)`` or ``None``."""

	assert isinstance(jtbd, str), "jtbd must be a string"
	assert isinstance(event, str), "event must be a string"
	for route in REPLY_ROUTES:
		if route.jtbd_id == jtbd and route.event == event:
			return route
	return None
