def lookup(slash: str, event: str) -> CommandSpec | None:
	"""Return the catalog entry matching ``/<slash> <event>`` or ``None``."""

	assert isinstance(slash, str), "slash must be a string"
	assert isinstance(event, str), "event must be a string"
	for spec in SLASH_COMMANDS:
		if spec.slash == slash and spec.event == event:
			return spec
	return None
